package blog.bean;

public class Blog {
	private String webname;
	private String webicons;
	private String author;
	private String authorImage;
	private String authorDiscription;
	private String mailAuthor;
	private String mail;
	private String phone;
	private String tel;
	private String address;
	private String year;
	
	public String getWebname() {
		return webname;
	}
	public void setWebname(String webname) {
		this.webname = webname;
	}
	public String getWebicons() {
		return webicons;
	}
	public void setWebicons(String webicons) {
		this.webicons = webicons;
	}
	public String getAuthor() {
		return author;
	}
	public void setAuthor(String author) {
		this.author = author;
	}
	public String getAuthorImage() {
		return authorImage;
	}
	public void setAuthorImage(String authorImage) {
		this.authorImage = authorImage;
	}
	public String getAuthorDiscription() {
		return authorDiscription;
	}
	public void setAuthorDiscription(String authorDiscription) {
		this.authorDiscription = authorDiscription;
	}
	public String getMailAuthor() {
		return mailAuthor;
	}
	public void setMailAuthor(String mailAuthor) {
		this.mailAuthor = mailAuthor;
	}
	public String getMail() {
		return mail;
	}
	public void setMail(String mail) {
		this.mail = mail;
	}
	public String getPhone() {
		return phone;
	}
	public void setPhone(String phone) {
		this.phone = phone;
	}
	public String getTel() {
		return tel;
	}
	public void setTel(String tel) {
		this.tel = tel;
	}
	public String getAddress() {
		return address;
	}
	public void setAddress(String address) {
		this.address = address;
	}
	public String getYear() {
		return year;
	}
	public void setYear(String year) {
		this.year = year;
	}
	
}
